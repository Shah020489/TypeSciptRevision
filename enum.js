/**Enum in typescript */
var TimeSlot;
(function (TimeSlot) {
    TimeSlot["morning"] = "9-12";
    TimeSlot["afternoon"] = "2-5";
    TimeSlot["evening"] = "7-10";
    TimeSlot[TimeSlot["phoneNumber"] = 920334] = "phoneNumber";
})(TimeSlot || (TimeSlot = {}));
console.log(TimeSlot.morning);
console.log(TimeSlot.phoneNumber);
console.log(TimeSlot.afternoon);
console.log(TimeSlot.evening);
